#include <bits/stdc++.h>
using namespace std;
int main()
{
    int n=4;
    for(int i=0;i<=n-1;i++){
        char ch='A';
        for(int j=1;j<=n;j++){
            cout<<ch<<" ";
            ch++;
        }
        cout<<endl;
    }
    
    return 0;
}